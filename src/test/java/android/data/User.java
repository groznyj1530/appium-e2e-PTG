package android.data;

public class User {
    private String code = "000";
    private String phone = "000000000";

    public void setCode(String code) {
        this.code = code;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCode() {
        return code;
    }

    public String getPhone() {
        return phone;
    }
}
